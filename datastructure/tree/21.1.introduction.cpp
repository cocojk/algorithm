// node, edge 
// parent - 상위 노드
// child - 하위 노드 
// sibling - 부모 노드가 같은 노드들 
// ancestor - 부모 노드와 그의 부모들 
// descendant - 자식와 그의 자식들 
// root - 다른 모든 노드들을 자손으로 갖는 노드 
// leaf - 자식이 하나도 없는 노드 
// depth - 루트에서 어떤 노드에 도달하기 위해 거쳐야하는 edge의 수 
// height - 트리에서 가장 깊숙히 있는 노드의 깊이 
// t를 루트로 하는 subtree - 어떤 노드 t와 그 자손들로 구성된 트리 
// 재귀적 속성 - 대개 재귀 호출을 이용해서 코드 구성 



